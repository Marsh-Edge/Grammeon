def get_exam_results(exam_type: str, questions: list, answers: list) -> dict:
    total = len(questions)
    correct_count = 0
    details = []

    for i, q in enumerate(questions):
        user_answer = answers[i] if i < len(answers) else -1
        is_correct = user_answer == q["correct"]
        if is_correct:
            correct_count += 1
        details.append({
            "question": q["question"],
            "options": q["options"],
            "correct": q["correct"],
            "user_answer": user_answer,
            "is_correct": is_correct,
            "explanation": q["explanation"],
            "type": q["type"],
            "passage": q.get("passage", ""),
        })

    score = correct_count
    percent = int(round(score / total * 100))

    if percent >= 90:
        feedback = f"Excellent! You're well-prepared for {EXAM_NAMES[exam_type]}."
    elif percent >= 70:
        feedback = f"Good job! You have a solid foundation. Review some areas before the real exam."
    elif percent >= 50:
        feedback = f"Not bad, but you need more practice for {EXAM_NAMES[exam_type]}."
    else:
        feedback = f"Keep studying! Focus on the basics before attempting {EXAM_NAMES[exam_type]}."

    return {
        "exam_type": exam_type,
        "score": score,
        "total": total,
        "percent": percent,
        "feedback": feedback,
        "details": details,
    }
